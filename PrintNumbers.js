//Print Numbers Using Loops

console.log("*** Print Numbers 1 to 10 ***")
console.log("* Using for Loop *")

for(var i = 1; i <= 10; i++){
	console.log(i)
}

console.log("* Using while Loop *")

var j = 1;

while(j <= 10){
	console.log(j)
	j++;
}

