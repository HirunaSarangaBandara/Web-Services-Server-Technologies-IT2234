//Basic Mathematical Operations

console.log("*** Basic Mathematical Operations ***")

console.log("* Using let Keyword *")


let num1 = 20;
let num2 = 15;

let sum = num1 + num2;
let sub = num1 - num2;
let div = num1 / num2;
let mul = num1 * num2;
let mod = num1 % num2;

console.log("Summation : " + sum)
console.log("Substraction : " + sub)
console.log("Division : " + div)
console.log("Multiplication : " + mul)
console.log("Modules : " + mod)

/* Output : 
	*** Basic Mathematical Operations ***
	* Using let Keyword *
	Summation : 35
	Substraction : 5
	Multiplication : 300
	Modules : 5
*/

